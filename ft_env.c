#include "minishell.h"

int	ft_env(void)
{
	unsigned int	i;

	i = 0;
	while (g_env[i])
	{
		ft_putstr_fd(g_env[i], 1);
		ft_putchar_fd('\n', 1);
		i++;
	}
	return (1);
}
